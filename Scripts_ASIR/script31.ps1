################ Calculo Maximo ##############

$p=get-process
$l=$p.length
echo $l
$max=0;
for($i=0; $i -lt $l; $i++){

$id=$p[$i].Id
if ($id -gt $max) { $max=$id}

}
Write-Host "El ID mas alto es" $max